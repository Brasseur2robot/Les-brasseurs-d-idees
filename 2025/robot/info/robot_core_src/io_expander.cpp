/******************************************************************************
   Module Global Variables
 ******************************************************************************/
Adafruit_AW9523 aw;

/******************************************************************************
   Functions Definitions
 ******************************************************************************/
void IoExpanderInit() {
  Serial.print("IOexpander|Init : ");
  if (aw.begin(IO_EXPANDER_ADD) == false)
  {
    Serial.print("Failed");
  }
  else
  {
    Serial.println("OK");
  }
  aw.pinMode(IOX_SD_CS, OUTPUT);
}

void IoExpanderSet(int pin, bool state) {
  aw.digitalWrite(pin, state);
}
